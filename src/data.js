function queryRestaurants({ max_calories, max_time, max_budget, tags }) {
    let filtered = restaurants;

    if (max_time) {
        filtered = filtered.filter(r => r.delivery_time <= max_time);
    }

    if (tags && tags.length > 0) {
        const normalize = str => str.toLowerCase().replace(/[^a-z0-9]/g, '');
        const normalizedTags = tags.map(normalize);
        filtered = filtered.filter(r =>
            r && r.tags && r.tags.some(tag => {
                const normTag = normalize(tag);
                return normalizedTags.some(nt => normTag.includes(nt) || nt.includes(normTag));
            })
        );
    }

    let results = [];
    for (const r of filtered) {
        if (!r || !r.menu_sections) continue;
        const validSections = [];
        for (const section of r.menu_sections) {
            if (!section.items) continue;
            const validItems = section.items.filter(item => {
                let valid = true;
                if (max_budget && item.price > max_budget) valid = false;
                if (max_calories && item.calories > max_calories) valid = false;
                return valid;
            });
            if (validItems.length > 0) {
                validSections.push({ ...section, items: validItems });
            }
        }
        if (validSections.length > 0) {
            results.push({ ...r, menu_sections: validSections });
        }
    }
    return results;
}

function getUserHistory(user_id) {
    return order_history.filter(o => o.user_id === user_id);
}

// Returns a taste profile for a user based on their order history
function getUserTasteProfile(user_id) {
    const history = getUserHistory(user_id);
    if (history.length === 0) return null;

    const restaurantFrequency = {};
    const itemFrequency = {};
    let totalRating = 0;

    for (const order of history) {
        restaurantFrequency[order.restaurant_name] = (restaurantFrequency[order.restaurant_name] || 0) + 1;
        itemFrequency[order.item_name] = (itemFrequency[order.item_name] || 0) + 1;
        totalRating += order.rating_given;
    }

    const favRestaurant = Object.entries(restaurantFrequency).sort((a, b) => b[1] - a[1])[0][0];
    const favItem = Object.entries(itemFrequency).sort((a, b) => b[1] - a[1])[0][0];
    const avgRating = (totalRating / history.length).toFixed(1);

    // Collect tags from frequently visited restaurants
    const tagFrequency = {};
    for (const r of restaurants) {
        if (restaurantFrequency[r.name]) {
            for (const tag of r.tags) {
                tagFrequency[tag] = (tagFrequency[tag] || 0) + restaurantFrequency[r.name];
            }
        }
    }
    const topTags = Object.entries(tagFrequency).sort((a, b) => b[1] - a[1]).slice(0, 3).map(e => e[0]);

    return {
        user_id,
        favourite_restaurant: favRestaurant,
        favourite_item: favItem,
        average_rating_given: parseFloat(avgRating),
        top_cuisine_tags: topTags,
        total_orders: history.length
    };
}

// Returns recurring orders (same item ordered 2+ times)
function getRecurringOrders(user_id) {
    const history = getUserHistory(user_id);
    const itemCount = {};
    for (const order of history) {
        const key = `${order.restaurant_name} — ${order.item_name}`;
        itemCount[key] = (itemCount[key] || 0) + 1;
    }
    return Object.entries(itemCount)
        .filter(([_, count]) => count >= 2)
        .map(([key, count]) => ({ order: key, times_ordered: count }));
}

